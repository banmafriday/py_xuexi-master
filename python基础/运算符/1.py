# 算术运算符
a = 5
b = 2
print(a / b)  # 除法
print(a // b)  # 除法，只保留整数
print(a % b)  # 获取余数
print(a ** b)  # 获取幂次方

# 赋值运算符
a = 123

# 关系运算符：比较变量之间的关系

aa = 5
bb = 6
print(aa > bb)

# 判断字符串是否相等
c1 = "hello"
d1 = "你好"
print(c1 == d1)
print(d1 != c1)

# 逻辑运算符 判断表达式之间的逻辑关系
# and or not
print(5 > 3 and 6 > 3)
print(5 > 3 or 6 < 3)
print(not 6>3)  # 对右边的布尔青大式取反
